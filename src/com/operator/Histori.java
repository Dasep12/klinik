    public void loadData(String data){
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Tanggal Berobat");
        model.addColumn("ID Pasien");
        model.addColumn("Nama");
        model.addColumn("Penyakit");
        model.addColumn("Tindak Lanjut");
        
        //tampilkan data makanan kedalam table 
        try {
            String sql = "" ;
            
            if(data == null ){
                sql = "SELECT * FROM tbl_pasien JOIN rekam_medis WHERE " +
                " tbl_pasien.id_pasien = rekam_medis.id_pasien" +
                " AND STATUS='SUDAH DI PERIKSA'" +
                " AND transaksi='OK' " ;
            }else {
                
                    sql = "SELECT * FROM tbl_pasien" +
                            " LEFT JOIN  rekam_medis ON tbl_pasien.id_pasien = rekam_medis.id_pasien " +
                            " WHERE tbl_pasien.id_pasien LIKE '%"+data+"%' OR tbl_pasien.nama LIKE '%"+data+"%' " +
                            " AND STATUS='SUDAH DI PERIKSA' " +
                            " AND transaksi='OK' "  ;
            }
                    java.sql.Connection conn = (Connection)Conn.configDB();
                    java.sql.Statement stm = conn.createStatement();
                    java.sql.ResultSet res = stm.executeQuery(sql);
                    while(res.next()){
                        model.addRow(new Object[] {res.getString("tgl_daftar") ,  res.getString("id_pasien") ,
                            res.getString("nama") ,  res.getString("nama_penyakit") , res.getString("tindak_lanjut")  } );
                    }
            obat.setModel(model);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        } 
    }
    
